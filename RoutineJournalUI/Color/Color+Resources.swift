  private static func load(_ name: String) -> Color {
    guard
      let color = UIColor(
        named: name,
        in: .resources,
        compatibleWith: nil
      )
    else {
      fatalError("Failed to load RoutineJournalUI color: \(name)")
    }
    return Color(color)
  }
