import SwiftUI

extension Color {
  static func load(_ name: String) -> Color {
    guard let color = UIColor(named: name, in: .ui, compatibleWith: nil) else {
      fatalError("Resource \(name) couldn't be loaded")
    }
    return Color(color)
  }
}

